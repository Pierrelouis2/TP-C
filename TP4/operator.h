#include <stdio.h>

int add(int num1, int num2){

return num1 + num2;

}

int min(int num1, int num2){

return num1 - num2;

}

int mul(int num1, int num2){

return num1 * num2;

}

int div(int num1, int num2){

return num1 / num2;

}

int mod(int num1, int num2){

return num1 % num2;

}

int et(int num1, int num2){

return num1 & num2;

}

int ou(int num1, int num2){

return num1 | num2;

}

int non(int num1){

return ~num1;

}
